export type TranslationKey = keyof typeof translations.en;

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(vars[token] ?? ""));
}

export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>) {
  const template = translations[locale][key] ?? translations.en[key] ?? key;
  return interpolate(template, vars);
}

export function formatRoomStatus(locale: Locale, status: RoomStatus) {
  return translate(locale, `status_${status}` as TranslationKey);
}

